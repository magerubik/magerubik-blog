namespace Magerubik\Blog\Block\Post\PostList;
/**
 * Post list item
 */
class Item extends \Magerubik\Blog\Block\Post\AbstractPost
{
	/**
	 * @return
	 */
	public function getMediaFolder() {
		$media_folder = $this->_storeManager->getStore()->getBaseUrl(\Magento\Framework\UrlInterface::URL_TYPE_MEDIA);
		return $media_folder;
	}
}